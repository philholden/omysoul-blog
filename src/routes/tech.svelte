<h1>What technology can I use to take church out on the street?</h1>
<img style="width: 100%" src="/img/omysoul-tech-plain.svg" />

<p>
  Have you ever noticed in church when the lyrics stop working everyone stops
  singing? Or how sometimes with projected lyrics people start singing a new
  song like its an old favourite?
</p>
<p>
  Our aim is to give you a wireless, battery powered, church service you can
  take out onto the streets. We also want:
</p>
<ul>
  <li>a three minute set up time</li>
  <li>kit that will fit in a single small back pack</li>
  <li>to use cheap off the shelf hardware you can buy on Amazon or Ebay</li>
</ul>

<p>
  Jesus was clever about how he used technology. When the crowds got so tight
  they blocked people from seeing and hearing, he got into a boat and put a
  little distance between himself and the crowd. That way he could hold the
  attention of more people.
</p>

