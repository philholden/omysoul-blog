<p>
  Most companies today see the need to foster diversity in the workplace. A
  company that makes people from diverse backgrounds feel they belong can retain
  staff and hire from a broader base. Practically this means HR is often
  supportive when employees step forward to create an affinity group that
  supports people with a common faith, gender, or sexuality. HR provides support
  by allowing these groups to book meeting rooms and use internal communication
  to make employees aware of the group's existence and programme.
</p>
<p>
