<h1>Is your lamp under a bowl?</h1>
<p>
  We aim to gather large numbers to worship Jesus in public spaces around the
  world. Jesus has a great promise:
</p>
<blockquote>
  Whoever
  <b>acknowledges</b>
  me before people, I will also
  <b>acknowledge</b>
  before my Father in heaven.
  <nobr>
    <i>(Matthew 10:32)</i>
  </nobr>
</blockquote>
<p>But also a dire warning:</p>
<blockquote>
  But whoever
  <b>disowns</b>
  me before men, I will
  <b>disown</b>
  him before my Father in heaven.
  <br />
  <nobr>
    <i>(Matthew 10:33)</i>
  </nobr>
</blockquote>
<p>
  We want to connect with others feel they need to "come out" as worshippers
  Jesus Christ.
</p>
<p>
  If you live in Edinburgh UK please put that in the location box and I will let
  you know about the first open air events. We will try to connect clusters of
  people in other areas. As well as keep you up to date with developments.
</p>

